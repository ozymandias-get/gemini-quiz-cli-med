export type TranslationContent = typeof TR_CONTENT;

export const TRANSLATIONS: Record<LanguageCode, TranslationContent> = {
  tr: TR_CONTENT,
  en: EN_CONTENT,
};

export function getStoredLanguageCode(): LanguageCode {
  try {
    const stored = localStorage.getItem(LANGUAGE_STORAGE_KEY);
    if (stored === 'tr' || stored === 'en') return stored;
  } catch {
    /* ignore */
  }
  return 'tr';
}

export function getCurrentTranslations(language?: LanguageCode): TranslationContent {
  return TRANSLATIONS[language ?? getStoredLanguageCode()];
}

export function getModelDisplayName(model: ModelType, t: TranslationContent): string {
  switch (model) {
    case ModelType.PRO:
      return t.proModel;
    case ModelType.FLASH:
      return t.flashModel;
    case ModelType.FLASH_2_5:
      return t.flash25Model;
    case ModelType.LITE:
      return t.liteModel;
    default:
      return t.flashModel;
  }
}
